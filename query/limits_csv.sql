!date ;

SET ECHO ON
SET TIMING ON
SET LINES 120
SET PAGES 100
SET COLSEP ","
SET FEEDBACK OFF
SET NULL "NULL"
CLEAR COL
COL PROFILE FORMAT A15
COL RESOURCE_NAME FORMAT A30
COL RESOURCE_TYPE FORMAT A15
COL LIMIT FORMAT A15
SELECT
  PROFILE,
  RESOURCE_NAME,
  RESOURCE_TYPE,
  LIMIT
FROM
  DBA_PROFILES
WHERE
  PROFILE = 'DEFAULT'
ORDER BY
  RESOURCE_TYPE, RESOURCE_NAME
/
EXIT
